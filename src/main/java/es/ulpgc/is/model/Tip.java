package es.ulpgc.is.model;

public class Tip {
    private Driver chofer;
    private double tip;

    public Tip() {
        this.chofer = chofer;
        this.tip = tip;
    }

    public double getTip() {
        return tip;
    }

    public void setTip(double tip) {
        this.tip = tip;
    }

    public Driver chofer() {
        return chofer;
    }

    public void setChofer(Driver chofer) {
        this.chofer = chofer;
    }
}
